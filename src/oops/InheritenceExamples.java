package oops;

public class InheritenceExamples {
	/*
	 * 1.Single Inheritance
	 * 
	 * Class B extends Class A
	 * 
	 * 2.Multiple Inheritance
	 * 
	 * Class A : Class B
	 * 
	 * Class C extends A and extends B
	 * 
	 * 3.Multilevel Inheritance
	 * 
	 * Class C extends B and extends A
	 * 
	 * 4.Hierarchical Inheritance
	 * 
	 * Class A :
	 * 
	 * Class B extends A
	 * 
	 * Class C extends A
	 * 
	 * 5.Hybrid Inheritance
	 * 
	 * Class C extends B extends Class A
	 * 
	 * Class F extends E extends Class C
	 * 
	 * Class F extends D extends Class C
	 * 
	 */

	/*
	 * In Classes : Multiple,Cyclic and Hybrid Inheritance not allowed in Java
	 * 
	 * In Interfaces : Multiple and Hybrid Inheritance allowed in Java
	 * 
	 */

	/*
	 * Cyclic Inheritance
	 * 
	 * 1.Class A extends A
	 * 
	 * 2.Class A extends B : Class B extends A
	 * 
	 */
}

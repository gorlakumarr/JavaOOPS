package javaSessionsBasics;

public class IfElseConcepts {
	/*
	 * > < >= <= == !=
	 */
	public static void main(String[] args) {

		int a = 30;
		int b = 20;

		if (a > b)
			System.out.println("a is greater than b");
		else if (a < b)
			System.out.println("b is greater than a");
		else
			System.out.println("a is equals to b");

	}
}

package javaSessionsBasics;

public class DataTypes {
	/*
	 * This is my first java Code
	 */

	// Main method is the Starting point of the programme
	public static void main(String[] args) {

		// 1. Int
		int i = 10;
		i = 20;
		System.out.println(i);

		int j = 30;
		int k = 40;
		int h = -1;
		int o = 0;

		// 2. Double
		double d1 = 12.3;
		double d2 = -12.33;
		double d3 = 100;// i can store Integer in double. it will be 100.0
		System.out.println(d3);

		// 3.Char : Only Single Digit Value and should be written in Single Quotes
		char c1 = 'A';
		char c2 = '1';
//		char c3 = '11';//Invalid character constant
		System.out.println(c2);

		// 4.Boolean :(True/False are Keywords)
		boolean b1 = false;
		boolean b2 = true;
		System.out.println(b2);

		// 5.String is Class(Not A Data Type) : Should be written in double qoutes
		String s1 = "Java Session";
		String s2 = "100";
		String s3 = "100.10";
		System.out.println(s3);

	}
}
